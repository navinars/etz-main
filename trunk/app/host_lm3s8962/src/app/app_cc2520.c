static OS_STK  Task_CC2520Stk[TASK_CC2520_STK_SIZE];



/* ------------------------------------------------------------------------------------------------------
 *											Local Functions
 * ------------------------------------------------------------------------------------------------------
 */
static  void  App_TaskCC2520 (void *p_arg);


